const idade = 22;

// estrutura if-else
if (idade < 12){
    console.log("Criança")
}else{
    console.log("Não é criança")
}

// estrutura if - else if - else
if (idade < 12){
    console.log("Criança")
}else if (idade >= 12 && idade < 18){
    console.log("Adolescente")
}else{
    console.log("Adulto")
}